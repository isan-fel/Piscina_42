void	ft_sort_int_tab(int *tab, int size)
{
	int n;
	int i;
	int x;

	n = 0;
	i = 1;
	while (n < size - 1)
	{
		if (tab[n] > tab[i])
		{
			x = tab[i];
			tab[i] = tab[n];
			tab[n] = x;
			n = -1;
			i = 0;
		}
		++n;
		++i;
	}
}
