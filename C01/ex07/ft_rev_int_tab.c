void	ft_rev_int_tab(int *tab, int size)
{
	int n;
	int rev[size];

	n = 0;
	while (n < size)
	{
		rev[n] = tab[size - n - 1];
		++n;
	}
	n = 0;
	while (n < size)
	{
		tab[n] = rev[n];
		++n;
	}
}
